package com.app.brandmania.Activity.admin;

public class NotificationFlag {
    private String id;
    private String notificationFlag;
    private String name;

    public String getId() {
        return id;
    }

    public NotificationFlag setId(String id) {
        this.id = id;
        return this;
    }

    public String getNotificationFlag() {
        return notificationFlag;
    }

    public NotificationFlag setNotificationFlag(String notificationFlag) {
        this.notificationFlag = notificationFlag;
        return this;
    }

    public String getName() {
        return name;
    }

    public NotificationFlag setName(String name) {
        this.name = name;
        return this;
    }
}
